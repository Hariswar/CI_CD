import math 

def add (a,b):
    return a + b

def subtract (a, b):
    return a - b

def multiply (a, b):
    return a * b

def divide (a, b):
    return a / b

def square (a):
    return a * a

def square_root(a):
    return math.sqrt(a)

def math_sin(a):
    return math.sin(a)

def math_cos(a):
    return math.cos(a)
